bool isprime(int ToE)
{
    if (ToE <= 1)
    {
        return false;
    }
    for (long ToS = 2; ToS <= sqrt(ToE); ToS++)
    {
        if (ToE % ToS == 0)
        {
            return false;
        }
    }
    return true;
}



